using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace senac_machine_learning_PI3.Models
{
    public class Neuron
    {
        public int Id { get; set; }
        public int Class { get; set; }
        public double[] Weights { get; set; }
        public string Color { get; set; }
    }

    public static class Extension {
        public static Neuron GetNeuron(this Neuron[] neurons, int line, int col)
        {
            return neurons[(int)Math.Sqrt(neurons.Count()) * line + col];
        }

        public static string GetLineNeuronsAsString(this Neuron[] neurons, int line, int size)
        {
            var result = "";
            for (int col = 0; col < size; col++)
            {
                result += "[" + neurons[size * line + col].Class + "],";
            }
            return result;
        }
    }

}
